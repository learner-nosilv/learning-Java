package variable;

public class Var5 {
    public static void main(String[] args) {
        // 1. 변수 선언, 초기화 따로
        int a;
        a = 10;
        System.out.println(a);

        // 2. 변수 선언 + 초기화 한 번에
        int b = 20;
        System.out.println(b);

        // 3. 여러 번수를 한 번(;)에 선언 + 초기화
        int c = 30, d = 40;
        System.out.println(c);
        System.out.println(d);

    }
}
