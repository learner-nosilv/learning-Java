package variable;

public class Var2 {
    public static void main(String[] args) {
        int a;  // [변수 선언] 정수를 담을 수 있는 그릇 만들기
        a = 20;   // [변수 초기화] 대입 | 그릇에 숫자 넣기 (10 -> 20)

        System.out.println(a);  // [변수값 출력] 그릇 속의 숫자를 출력하기
        System.out.println(a);
        System.out.println(a);
    }
}
